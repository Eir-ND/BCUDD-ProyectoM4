class booking {
    constructor(
        id, 
        nameHotel, 
        arrivalDate, 
        departureDate, 
        passengers, 
        typeRoom, 
        name, 
        email, 
        paymentStatus
    ) {
        this.id = id;
        this.nameHotel = nameHotel;
        this.arrivalDate = arrivalDate;
        this.departureDate = departureDate;
        this.passengers = passengers;
        this.typeRoom = typeRoom;
        this.name = name;
        this.email = email;
        this.paymentStatus = paymentStatus;
    };
};